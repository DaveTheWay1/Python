# Range in python

# one
for i in range(5):
    print(i)
# output: 01234

# Two arguments
for i in range(2, 7):
    print(i)
# output:23456

# Three arguments
for i in range(2, 16, 3):
    print(i)

# output: 2 5 8 11 14

# decrementing
for x in range(0, 10, 2):
    print(x)
# output: 0, 2, 4, 6, 8
for x in range(5, 1, -3):
    print(x)
# output: 5, 2